class Variable:
    def __init__(self, data):
        self.data = data
        self.grad = None
        self.creator = None

    def set_creator(self, func):
        self.creator = func

    def backward(self):
        f = self.creator
        if f is not None:
            x = f.input  # 関数の入力を取得
            x.grad = f.backward(self.grad)  # 関数のbackwardを呼び出す
            x.backward()  # 自分より１つ前の変数のbackwardを再帰的に呼び出す


class Function:
    def __call__(self, input):
        x = input.data
        y = self.forward(x)
        output = Variable(y)
        output.set_creator(self)
        self.input = input
        self.output = output
        return output

